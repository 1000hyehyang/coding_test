"""
크기가 N×M인 지도가 존재한다. 지도의 오른쪽은 동쪽, 위쪽은 북쪽이다. 이 지도의 위에 주사위가 하나 놓여져 있으며, 주사위의 전개도는 아래와 같다. 지도의 좌표는 (r, c)로 나타내며, r는 북쪽으로부터 떨어진 칸의 개수, c는 서쪽으로부터 떨어진 칸의 개수이다. 

  2
4 1 3
  5
  6
주사위는 지도 위에 윗 면이 1이고, 동쪽을 바라보는 방향이 3인 상태로 놓여져 있으며, 놓여져 있는 곳의 좌표는 (x, y) 이다. 가장 처음에 주사위에는 모든 면에 0이 적혀져 있다.

지도의 각 칸에는 정수가 하나씩 쓰여져 있다. 주사위를 굴렸을 때, 이동한 칸에 쓰여 있는 수가 0이면, 주사위의 바닥면에 쓰여 있는 수가 칸에 복사된다. 0이 아닌 경우에는 칸에 쓰여 있는 수가 주사위의 바닥면으로 복사되며, 칸에 쓰여 있는 수는 0이 된다.

주사위를 놓은 곳의 좌표와 이동시키는 명령이 주어졌을 때, 주사위가 이동했을 때 마다 상단에 쓰여 있는 값을 구하는 프로그램을 작성하시오.

주사위는 지도의 바깥으로 이동시킬 수 없다. 만약 바깥으로 이동시키려고 하는 경우에는 해당 명령을 무시해야 하며, 출력도 하면 안 된다.
"""
import sys

input = sys.stdin.readline
n, m, x, y, k = map(int, input().split())
map_list = [list(map(int, input().split())) for _ in range(n)]
commands = list(map(int, input().split())) # 1: 동쪽, 2: 서쪽, 3: 북쪽, 4: 남쪽

dice = [0, 0, 0, 0, 0, 0] # 윗면, 북쪽면, 동쪽면, 남쪽면, 서쪽면, 바닥면

"""
주사위를 동쪽으로 굴리면 윗면 -> 동쪽면 / 북쪽면 -> 그대로 / 동쪽면 -> 바닥면 / 남쪽면 -> 그대로 / 서쪽면 -> 윗면
주사위를 서쪽으로 굴리면 윗면 -> 서쪽면 / 북쪽면 -> 그대로 / 동쪽면 -> 윗면 / 남쪽면 -> 그대로 / 서쪽면 -> 바닥면
주사위를 북쪽으로 굴리면 윗면 -> 북쪽면 / 북쪽면 -> 바닥면 / 동쪽면 -> 그대로 / 남쪽면 -> 윗면 / 서쪽면 -> 그대로
주사위를 남쪽으로 굴리면 윗면 -> 남쪽면 / 북쪽면 -> 그대로 / 동쪽면 -> 그대로 / 남쪽면 -> 바닥면 / 서쪽면 -> 윗면

주사위를 굴리는 방향에 따라 윗면은 그 방향의 편이 되고, 바닥면은 그 방향의 반대편이 된다.
"""

# 방향별 이동: 동, 서, 북, 남
dx = [0, 0, -1, 1]
dy = [1, -1, 0, 0]

for command in commands:
    direction = command - 1
    
    nx = x + dx[direction]
    ny = y + dy[direction]
    
    # 경계 체크: 지도 범위를 벗어나면 무시
    if nx < 0 or nx >= n or ny < 0 or ny >= m:
        continue
    
    if direction == 0:  # 동쪽
        dice[0], dice[2], dice[5], dice[4] = dice[4], dice[0], dice[2], dice[5]
    elif direction == 1:  # 서쪽
        dice[0], dice[2], dice[5], dice[4] = dice[2], dice[5], dice[4], dice[0]
    elif direction == 2:  # 북쪽
        dice[0], dice[1], dice[5], dice[3] = dice[3], dice[0], dice[1], dice[5]
    elif direction == 3:  # 남쪽
        dice[0], dice[1], dice[5], dice[3] = dice[1], dice[5], dice[3], dice[0]
    
    x, y = nx, ny
    
    # 지도 칸의 값과 주사위 바닥면 값 교환
    if map_list[x][y] == 0:
        # 칸이 0이면 주사위 바닥면 값을 칸에 복사
        map_list[x][y] = dice[5]
    else:
        # 칸이 0이 아니면 칸의 값을 주사위 바닥면에 복사하고 칸은 0으로
        dice[5] = map_list[x][y]
        map_list[x][y] = 0
    
    # 주사위 윗면 값 출력
    print(dice[0])
